// What is a function?
    // a function lets us reuse code.
    // makes our code cleaner by removing duplicated code.
        // if we wanted to run the code again, we just type the function name again with brackets. Example:
            // function1();
            // function1();
                // this will run the code twice.

// Syntax Rules for Functions
    // function -> creates a new function
        // then we choose a name (cannot use the word 'function', no starting with numbers or special characters.)
    // use camelCase
    // inside the curly brackets, we put the code that we want to run when calling the function.

    // IMPORTANT: the function will ONLY RUN when its called. Ex. calling function1(); will run the content inside it.

// Return Statement:
    // allows us to get a value out of a function.

// Syntax rules for a Return Statement:
    // just type 'return' and a value. Example: return 'rock'; or return variable1, etc.
        // returning a value from a function, or the 'return value'.
        // We don't always have to return a value, just calling return; will result in 'undefined.
    // When we use a return statement, it ends the function IMMEDIATELY.


// Parameters; puts a value INTO a function; opposite to a return statement.
    // works the same way as a variable.
    // Example:
        // function calculateTax(parameter1) {
           // console.log(parameter1 * 0.10) // 10.00 into cents.
       // }
       // calculateTax(2000); // runs the function // Output: 100 cents

// When a function has a parameter, we usually say this function TAKES a parameter. If this paramenter should be a number, it should be this function takes a number. When we put a value into the function, its called PASSING the value into a function.

